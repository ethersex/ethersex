#include "timer.h"

void timer_init(void)
/* {{{ */ {

    /* init timer1 to expire after 200ms, with CTC enabled */
    TCCR1B = _BV(WGM12) | _BV(CS12) | _BV(CS10);
    OCR1A = (F_CPU/1024/5);

} /* }}} */
